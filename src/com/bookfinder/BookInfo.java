package com.bookfinder;

public class BookInfo {

	private int bookID;
	private String bookName;
	private String author;
	private String version;
	private String bookImage;
	private String description;
	private String level;
	private String section;
	private String shelve ;
	private String bookNumber ;
	private String nfcReferencecode ;
	
	public int getBookID() {
		return bookID;
	}

	public void setBookID(int bookID) {
		this.bookID = bookID;
	}

	public String getBookName() {
		return bookName;
	}

	public void setBookName(String bookName) {
		this.bookName = bookName;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getBookImage() {
		return bookImage;
	}

	public void setBookImage(String bookImage) {
		this.bookImage = bookImage;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getSection() {
		return section;
	}

	public void setSection(String section) {
		this.section = section;
	}

	public String getShelve() {
		return shelve;
	}

	public void setShelve(String shelve) {
		this.shelve = shelve;
	}

	public String getBookNumber() {
		return bookNumber;
	}

	public void setBookNumber(String bookNumber) {
		this.bookNumber = bookNumber;
	}

	public String getNfcReferencecode() {
		return nfcReferencecode;
	}

	public void setNfcReferencecode(String nfcReferencecode) {
		this.nfcReferencecode = nfcReferencecode;
	}

}
